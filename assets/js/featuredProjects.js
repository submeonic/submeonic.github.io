document.addEventListener("DOMContentLoaded", function () {
    const categorySelect = document.getElementById('project-category');
    const projectsContainer = document.querySelector('.projects-view-container');

    // Function to populate project tiles based on the selected category
    function populateProjectTiles(projectData) {
        projectsContainer.innerHTML = ''; // Clear existing project tiles
        if (!projectData || Object.keys(projectData).length === 0) {
            console.error('No data available for this category: ' + projectData);
            projectsContainer.innerHTML = '<p>No projects to display for this category.</p>'; // Provide feedback to the user
            return;
        }
        
        let firstProjectId = null; // To store the ID of the first project
        Object.keys(projectData).forEach((projectId, index) => {
            const project = projectData[projectId];
            const projectDiv = document.createElement('div');
            projectDiv.className = 'project-container';
            projectDiv.innerHTML = `
                <h4>${project.title}</h4>
                <img src="${project.thumbnail}" alt="${project.title} Thumbnail" class="project-thumbnail">
            `;
            projectDiv.dataset.projectId = projectId; // Set data attribute for later reference
            projectDiv.addEventListener('click', () => {
                showProjectDetails(projectId, projectData, projectDiv);
            });
            projectsContainer.appendChild(projectDiv);

            // Automatically show details for the first project when category changes
            if (index === 0) {
                firstProjectId = projectId; // Save first project ID
            }
        });

        // If there's a first project, show its details and add the 'selected' class
        if (firstProjectId) {
            const firstProjectDiv = projectsContainer.querySelector(`[data-project-id="${firstProjectId}"]`);
            showProjectDetails(firstProjectId, projectData, firstProjectDiv);
        }
    }

    // Function to show project details when a project tile is clicked
    function showProjectDetails(projectId, projectData, projectDiv) {
        const project = projectData[projectId];
        document.querySelector('.project-details-title').textContent = project.title;
        document.querySelector('.project-details-subtitle').textContent = project.subtitle;
        document.querySelector('.project-details-video').src = project.videoUrl;
        document.querySelector('.project-details-role').textContent = project.roles;
        document.querySelector('.project-details-stack').textContent = project.stack;
        document.querySelector('.project-details-overview').textContent = project.overview;
        document.querySelector('.project-details-contributors').textContent = project.contributors;

        // Remove 'selected' class from all project divs and add to the clicked one
        document.querySelectorAll('.project-container').forEach(p => p.classList.remove('selected'));
        projectDiv.classList.add('selected');

        const repoButton = document.getElementById('project-repo-button');
        const tryButton = document.getElementById('project-try-button');

        repoButton.dataset.url = project.repositoryUrl || '#';
        repoButton.style.display = project.repositoryUrl ? 'inline-block' : 'none';

        tryButton.dataset.url = project.tryUrl || '#';
        tryButton.style.display = project.tryUrl ? 'inline-block' : 'none';
    }

    // Event listener for category selection change using direct object access
    categorySelect.addEventListener('change', function() {
        let selectedCategoryData;
        switch (this.value) {
            case 'InteractiveDesignProjectData':
                selectedCategoryData = InteractiveDesignProjectData;
                break;
            case 'GameDevelopmentProjectData':
                selectedCategoryData = GameDevelopmentProjectData;
                break;
            case 'ResearchProjectData':
                selectedCategoryData = ResearchProjectData;
                break;
            default:
                selectedCategoryData = null;
                break;
        }

        populateProjectTiles(selectedCategoryData);
    });

    // Initial loading of project tiles and showing details for the first project
    function initialLoad() {
        let initialData;
        switch (categorySelect.value) {
            case 'InteractiveDesignProjectData':
                initialData = InteractiveDesignProjectData;
                break;
            case 'GameDevelopmentProjectData':
                initialData = GameDevelopmentProjectData;
                break;
            case 'ResearchProjectData':
                initialData = ResearchProjectData;
                break;
            default:
                initialData = null;
                break;
        }
        populateProjectTiles(initialData);
    }

    initialLoad();
});


